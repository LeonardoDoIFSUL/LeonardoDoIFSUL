//Exponenciação nova | ES6
let valor = 2
//Quando o valor representa ele mesmo e uma operação ele é chamado Unário
valor **= 3 
console.log("Novo modo:",valor)

//Exponenciação antiga 
let antigoValor = Math.pow(3, 2)
console.log("Modo antigo:",antigoValor)

//Aplicado em uma equação
const eq = 2 ** -5;//Exponente negativo
console.log("Em equações: ",eq)